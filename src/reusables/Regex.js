export const passwordRegex =
  /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@.#$!%*?&])[A-Za-z\d@.#$!%*?&]{8,20}$/; //minim one lowercase & one uppercase character & and lenght netween 8 and 20 characters
export default {
  emailRegex,
  passwordRegex,
};
